
public class operation {

	public static void main(String[] args) {
		 //int/int and int%int
		 int a=5;
		 int b=2;
		 System.out.println(" int/int "+a/b);
		 System.out.println(" int%int "+a%b);
		 System.out.println();
		 
		 //int/double and int%double
		 int c=5;
		 double d=2;
		 System.out.println(" int/double "+c/d);
		 System.out.println(" int%double "+c%d);
		 System.out.println();
		 
		 //double/int  and double%int
		 double e=5;
		 int f=2;
		 System.out.println(" double/int "+e/f);
		 System.out.println(" double%int "+e%f);
		 System.out.println();
		 
		 //int/float and int%float
		 int g=5;
		 float h=2;
		 System.out.println(" int/float "+g/h);
		 System.out.println(" int%float "+g%h);
		 System.out.println();
		 
		 //float/int and float%int
		 float i=5;
		 int j=2;
		 System.out.println(" float/int "+i/j);
		 System.out.println(" float%int "+i%j);
		 System.out.println();
		 
		 //float/float and float%float
		 float k=5;
		 float l=2;
		 System.out.println(" float/float  "+k/l);
		 System.out.println(" float%float "+k%l);
		 System.out.println();
		 
		 //double/double and double%double
		 double m=5;
		 double n=2;
		 System.out.println(" double/double  "+m/n);
		 System.out.println(" double%double "+m%n);
		 System.out.println();
		 
		//long/long and long%long
		 long o=5;
		 long p=2;
		 System.out.println(" int/int "+o/p);
		 System.out.println(" int%int "+o%p);
		 System.out.println();

		//long/double and long%double
		 long q=5;
		 double r=2;
		 System.out.println(" long/double "+q/r);
		 System.out.println(" long%double "+q%r);
		 System.out.println();

	}

}
